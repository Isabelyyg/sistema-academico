    private void btRelatorioPacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRelatorioPacientesActionPerformed
        String email = InputEmail.getText().trim();
        String senha = new String(InputSenha.getPassword()).trim();

        if (email.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Email e senha são obrigatórios.",
                    "Campos obrigatórios",
                    JOptionPane.WARNING_MESSAGE);
            return; // Sai do método sem tentar autenticar
        }

        try {
            Usuario usuarioAutenticado = new LoginController().autenticar(email, senha);
            
            // armazena o usuario autenticado
            SessaoUsuario.getInstance().setUsuarioLogado(usuarioAutenticado);

            // Se chegou aqui, deu certo!
            JOptionPane.showMessageDialog(this,
                    "Login realizado com sucesso!",
                    "Bem-vindo(a)",
                    JOptionPane.INFORMATION_MESSAGE);

            FormPublicacoes novaTela = new FormPublicacoes();
            novaTela.setSize(this.getSize());        // <-- Mesmo tamanho da tela anterior
            novaTela.setLocation(this.getLocation()); // <-- Mesma posição (opcional)
            novaTela.setVisible(true);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Erro no Login",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btRelatorioPacientesActionPerformed

